package aulas.aula7;

public class OperadoresAritmeticos {
    public static void main(String[] args) {
        int n1 = 3;
        int n2 = 5;
        float m = (n1 + n2) / 2;

        System.out.println(m);

        // Operadores Unários


        int numero= 5;
        int valor = 5 + ++numero;

        System.out.println(valor);

        // operadores de atribuição

        int x = 4;
        x += 2; // igual a x = x + 2;

        System.out.println(x);


    }
}
